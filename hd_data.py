# Якорь: Gate 25 начинается на тропической долготе 358.25° (28°15' Рыб).
# Каждые ворота занимают 360/64 = 5.625°, каждая линия — 5.625/6 = 0.9375°.
WHEEL_START_LONGITUDE = 358.25
GATE_SPAN = 360.0 / 64.0      # 5.625
LINE_SPAN = GATE_SPAN / 6.0   # 0.9375

# Последовательность ворот по часовой стрелке от точки старта (не 1..64 подряд —
# это особенность "квантового" размещения мандалы).
GATE_WHEEL_SEQUENCE = [
    25, 17, 21, 51, 42, 3, 27, 24, 2, 23, 8, 20, 16, 35, 45, 12,
    15, 52, 39, 53, 62, 56, 31, 33, 7, 4, 29, 59, 40, 64, 47, 6,
    46, 18, 48, 57, 32, 50, 28, 44, 1, 43, 14, 34, 9, 5, 26, 11,
    10, 58, 38, 54, 61, 60, 41, 19, 13, 49, 30, 55, 37, 63, 22, 36,
]
assert len(GATE_WHEEL_SEQUENCE) == 64
assert len(set(GATE_WHEEL_SEQUENCE)) == 64  # все 64 ворот присутствуют ровно по разу

# 36 каналов: (ворота_A, ворота_B)
CHANNELS = [
    (1, 8), (2, 14), (3, 60), (4, 63), (5, 15), (6, 59), (7, 31), (9, 52),
    (10, 20), (10, 34), (10, 57), (11, 56), (12, 22), (13, 33), (16, 48),
    (17, 62), (18, 58), (19, 49), (20, 34), (20, 57), (21, 45), (23, 43),
    (24, 61), (25, 51), (26, 44), (27, 50), (28, 38), (29, 46), (30, 41),
    (32, 54), (34, 57), (35, 36), (37, 40), (39, 55), (42, 53), (47, 64),
]
assert len(CHANNELS) == 36

# Ворота → центр (проверено через перекрёстную сверку с CHANNELS: каждый канал
# должен соединять РОВНО два разных центра — см. self-test внизу файла).
GATE_TO_CENTER = {}
_CENTER_GATES = {
    "head":         [64, 61, 63],
    "ajna":         [47, 24, 4, 17, 43, 11],
    "throat":       [62, 23, 56, 12, 16, 20, 31, 8, 33, 35, 45],
    "g":            [1, 13, 25, 46, 2, 15, 10, 7],
    "heart":        [21, 40, 26, 51],
    "sacral":       [34, 5, 14, 29, 59, 9, 3, 42, 27],
    "solar_plexus": [6, 22, 30, 36, 37, 49, 55],
    "spleen":       [48, 57, 44, 50, 32, 28, 18],
    "root":         [58, 38, 54, 53, 60, 52, 19, 39, 41],
}
for _center, _gates in _CENTER_GATES.items():
    for _g in _gates:
        GATE_TO_CENTER[_g] = _center

MOTOR_CENTERS = {"sacral", "heart", "solar_plexus", "root"}

CENTERS_ALL = list(_CENTER_GATES.keys())

# Channels touching the Throat (used for Manifestor/MG detection)
THROAT_CHANNELS = [c for c in CHANNELS
                    if GATE_TO_CENTER[c[0]] == "throat" or GATE_TO_CENTER[c[1]] == "throat"]


def _self_test():
    """Каждый канал обязан соединять два РАЗНЫХ центра — иначе таблица сломана."""
    assert len(GATE_TO_CENTER) == 64
    for a, b in CHANNELS:
        ca, cb = GATE_TO_CENTER[a], GATE_TO_CENTER[b]
        assert ca != cb, f"Channel {a}-{b} maps to same center {ca} — data error"


_self_test()
